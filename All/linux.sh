ls //Lista os programas e diretórios - vem de (listing) em inglês

ls -l //Listagem longa - vem de (long listing) em inglês

ls -la //Listagem longa + os arquivos ocultos  //Lista os programas e diretórios de maneira longa e mostrando os arquivos ocultos - o -l vem de (long) e -a vem de (all) em inglês  

ls -lrt //Lista os arquivos e diretórios de maneira longa (long), reversa (reverse) e mostra o tempo (time)

ls / /boot //Faz a listagem dos diretórios  

cp //Copiar (copy) em inglês 

